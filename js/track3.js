function L7(){
    myDivObj = document.getElementById("L7")
    let myDivObjBgColor = window.getComputedStyle(myDivObj).backgroundColor;

    if(myDivObjBgColor == 'rgb(154, 205, 50)')
    {
        document.getElementById("L7").style.backgroundColor='green';
    }else{
        document.getElementById("L7").style.backgroundColor='yellowgreen';
    }
}

